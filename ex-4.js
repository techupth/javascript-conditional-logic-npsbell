// Switch Statement
let lightBulbStatus = "On";

switch (lightBulbStatus) {
    case lightBulbStatus = "On":
        "Light bulb is On.";
    case lightBulbStatus = "Off":
        "Light bulb is Off.";
    case lightBulbStatus = "Broken":
        "Light bulb is Broken.";
    break;
    default:
        "Please choose the correct input (On/Off/Broken)";
}
console.log(lightBulbStatus)
